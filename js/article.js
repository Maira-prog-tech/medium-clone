// Получаем ID статьи из URL
function getArticleId() {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get('id');
}

// Загружаем содержимое статьи
function loadArticle() {
    const articleId = getArticleId();
    const article = articleContents[articleId];

    if (!article) {
        window.location.href = 'index.html';
        return;
    }

    document.title = article.title + ' - Medium Clone';
    document.getElementById('articleTitle').textContent = article.title;
    document.getElementById('articleAuthor').textContent = article.author;
    document.getElementById('articleDate').textContent = article.date;
    document.getElementById('articleContent').innerHTML = article.content;
    document.getElementById('articleAuthorAvatar').src = 
        `https://ui-avatars.com/api/?name=${encodeURIComponent(article.author)}&background=random`;
}

// Инициализация страницы
document.addEventListener('DOMContentLoaded', loadArticle);
